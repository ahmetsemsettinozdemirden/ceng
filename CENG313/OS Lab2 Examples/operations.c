#include"operations.h"

float add(float number1, float number2){
    return number1 + number2;
}

float sub(float number1, float number2){
    return number1 - number2;
}

float mult(float number1, float number2){
    return number1 * number2;
}

float divi(float number1, float number2){
    return number1 / number2;
}
